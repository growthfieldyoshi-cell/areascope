/**
 * ai-corrections.csv から station_reading_overrides への INSERT SQL を生成する
 *
 * Usage:
 *   node scripts/build-overrides-from-csv.mjs
 *
 * 入力: scripts/ai-corrections.csv
 * 出力: scripts/insert-overrides.sql
 */
import fs from 'fs';
import { parse } from 'csv-parse/sync';

const INPUT = 'scripts/ai-corrections.csv';
const OUTPUT = 'scripts/insert-overrides.sql';

function escapeSQL(val) {
  if (val == null || val === '') return 'NULL';
  return `'${val.replace(/'/g, "''")}'`;
}

function main() {
  if (!fs.existsSync(INPUT)) {
    console.error(`入力ファイルが見つかりません: ${INPUT}`);
    process.exit(1);
  }

  const file = fs.readFileSync(INPUT, 'utf-8');
  const records = parse(file, { columns: true, skip_empty_lines: true });
  console.log(`読み込み: ${records.length}件`);

  const lines = [
    '-- Generated by build-overrides-from-csv.mjs',
    '-- station_reading_overrides への UPSERT',
    '',
  ];

  for (const r of records) {
    const slug = escapeSQL(r.station_group_slug);
    const hiragana = escapeSQL(r.station_name_hiragana);
    const katakana = escapeSQL(r.station_name_katakana);
    const initialKana = escapeSQL(r.station_name_initial_kana);
    const note = escapeSQL(r.note);

    lines.push(
      `INSERT INTO station_reading_overrides (station_group_slug, station_name_hiragana, station_name_katakana, station_name_initial_kana, note)` +
      ` VALUES (${slug}, ${hiragana}, ${katakana}, ${initialKana}, ${note})` +
      ` ON CONFLICT (station_group_slug) DO UPDATE SET` +
      ` station_name_hiragana = EXCLUDED.station_name_hiragana,` +
      ` station_name_katakana = EXCLUDED.station_name_katakana,` +
      ` station_name_initial_kana = EXCLUDED.station_name_initial_kana,` +
      ` note = EXCLUDED.note;`
    );
  }

  lines.push('');
  fs.writeFileSync(OUTPUT, lines.join('\n'), 'utf-8');

  console.log(`\n=== 完了 ===`);
  console.log(`出力: ${OUTPUT} (${records.length}件)`);
}

main();
